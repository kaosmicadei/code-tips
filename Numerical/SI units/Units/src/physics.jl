# hertz
const Hz = 1 / s
const kHz = 1e+3 / s
const MHz = 1e+6 / s

# angle
const rad = 1    # radian
const sr = 1     # steradian

# energy
const N = kg * m / s^2  # Newton
const Pa = N / m^2      # Pascal
const J = N * m         # Joule
const W = J / s

# electromagnetic
const C = A * s         # coulomb
const V = J / C         # volt
const F = C / V         # farad
const Ω = V / A         # ohm
const T = V * s / m^2   # tesla

# thermodynamics
const Celsius = 273.15K

# PHYSICAL CONSTANTS
const c = 299_792_458m/s      # speed of light
const h = 6.62607015e-34J*s   # Planck constant
const ħ = h / 2π
const e = 1.602176634e-19C    # elementar charge
const kb = 1.380649e-23J/K    # Boltzmann constant

# extra units
const eV = e*V  # electronvolt